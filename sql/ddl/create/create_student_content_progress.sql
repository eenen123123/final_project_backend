-- 수강생 콘텐츠 진도 추적 테이블
-- 클래스룸에서 수강생이 강의(LECTURE)를 완료할 때 이 테이블에 기록합니다.
-- 전체 학습 현황(/instructor/learning/overview) 집계에 사용됩니다.
--
-- LECTURE 테이블 PK가 (LECTURE_SN, COURSE_SN) 복합키라서
-- LECTURE_SN에 대한 FK 제약은 걸지 않고 소프트 참조로 처리합니다.
CREATE TABLE STUDENT_CONTENT_PROGRESS (
    PROG_SN       NUMBER(20)    GENERATED BY DEFAULT AS IDENTITY,
    CLASS_SN      NUMBER(20)    NOT NULL,   -- CLASSROOM.CLASS_SN (기수 구분)
    USER_ID       VARCHAR2(20)  NOT NULL,   -- MEMBER.USER_ID (수강생)
    LECTURE_SN    NUMBER(20)    NOT NULL,   -- LECTURE.LECTURE_SN (소프트 참조)
    COURSE_SN     NUMBER(20)    NOT NULL,   -- LECTURE.COURSE_SN (집계 조인용)
    CMPL_YN       CHAR(1)       DEFAULT 'N' NOT NULL,
    LAST_VIEW_DT  TIMESTAMP     NULL,
    CMPL_DT       TIMESTAMP     NULL,
    REG_DT        TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_STUDENT_CONTENT_PROGRESS PRIMARY KEY (PROG_SN),
    CONSTRAINT UQ_SCP_CLASS_USER_LECT UNIQUE (CLASS_SN, USER_ID, LECTURE_SN),
    CONSTRAINT FK_SCP_CLASS_SN FOREIGN KEY (CLASS_SN) REFERENCES CLASSROOM (CLASS_SN),
    CONSTRAINT FK_SCP_USER_ID  FOREIGN KEY (USER_ID)  REFERENCES MEMBER (USER_ID),
    CONSTRAINT CK_SCP_CMPL_YN  CHECK (CMPL_YN IN ('Y', 'N'))
);

COMMENT ON TABLE  STUDENT_CONTENT_PROGRESS              IS '수강생별 강의 완료 진도 기록';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.PROG_SN      IS 'PK · 시퀀스';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.CLASS_SN     IS 'CLASSROOM.CLASS_SN 참조 (동일 강좌 재수강 기수 구분)';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.USER_ID      IS 'MEMBER.USER_ID 참조 (수강생)';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.LECTURE_SN   IS 'LECTURE.LECTURE_SN 소프트 참조';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.COURSE_SN    IS 'LECTURE.COURSE_SN (집계 조인용)';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.CMPL_YN      IS 'Y: 완료 / N: 미완료';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.LAST_VIEW_DT IS '마지막 시청 시각 (클래스룸에서 갱신)';
COMMENT ON COLUMN STUDENT_CONTENT_PROGRESS.CMPL_DT      IS '완료 처리 시각';
