-- ================================================================
--  증명서 발급 (직원 셀프서비스)
--  · 직원 본인이 재직/경력/급여확인서를 직접 신청 → 즉시 자동발급
--  · 파일/스냅샷 보관 없음. 출력 시 EMPLOYEE_INFO 에서 정보 자동 조인
--  · 1회만 출력 가능 (PRN_YN)
-- ================================================================
CREATE TABLE CERTIFICATE_ISSUE (
    CERT_SN       NUMBER(20)     GENERATED BY DEFAULT AS IDENTITY,
    USER_ID       VARCHAR2(20)   NOT NULL,
    CERT_TY_CD    CHAR(2)        NOT NULL,
    ISSUE_RSN     VARCHAR2(1000) NULL,
    ISSUE_PURPS   VARCHAR2(500)  NULL,
    PRN_YN        CHAR(1)        DEFAULT 'N' NOT NULL,
    ISSUE_DT      TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
    PRN_DT        TIMESTAMP      NULL,
    RGTR_ID       VARCHAR2(20)   NULL,
    LAST_MDFR_ID  VARCHAR2(20)   NULL,
    REG_DT        TIMESTAMP      DEFAULT SYSTIMESTAMP NULL,
    MDFCN_DT      TIMESTAMP      NULL,
    CONSTRAINT PK_CERTIFICATE_ISSUE PRIMARY KEY (CERT_SN),
    CONSTRAINT FK_MEMBER_TO_CERTIFICATE_ISSUE FOREIGN KEY (USER_ID) REFERENCES MEMBER (USER_ID),
    CONSTRAINT CK_CERTIFICATE_ISSUE_PRN_YN CHECK (PRN_YN IN ('Y', 'N'))
);

COMMENT ON COLUMN CERTIFICATE_ISSUE.CERT_SN     IS '증명서 발급 일련번호 (PK)';
COMMENT ON COLUMN CERTIFICATE_ISSUE.USER_ID     IS '신청·발급 대상 직원 (MEMBER FK, 본인)';
COMMENT ON COLUMN CERTIFICATE_ISSUE.CERT_TY_CD  IS '증명서 종류 (공통코드 228 · 01:재직 02:경력 03:급여확인서)';
COMMENT ON COLUMN CERTIFICATE_ISSUE.ISSUE_RSN   IS '필요 사유';
COMMENT ON COLUMN CERTIFICATE_ISSUE.ISSUE_PURPS IS '제출처(사용처)';
COMMENT ON COLUMN CERTIFICATE_ISSUE.PRN_YN      IS '출력 여부 (Y:출력완료 N:미출력) · 1회 출력 제한';
COMMENT ON COLUMN CERTIFICATE_ISSUE.ISSUE_DT    IS '발급일시 (신청 즉시 자동발급)';
COMMENT ON COLUMN CERTIFICATE_ISSUE.PRN_DT      IS '출력일시';

CREATE INDEX IX_CERTIFICATE_ISSUE_USER ON CERTIFICATE_ISSUE (USER_ID, ISSUE_DT DESC);
