-- ============================================================
-- 학부모/학생 상담 관리 (manager/consultation.html)
--  · 상담 유형 : 공통코드 211 (01 대면 / 02 전화 / 03 문자)
--  · 상담 상태 : 공통코드 212 (01 예정 / 02 완료 / 03 후속조치 / 04 취소)
--  · 학생-학부모 매칭 : STUDENT.PRNT_USER_ID → MEMBER
--  · 담당 강사 : CHRG_USER_ID (상담 작성자/담당자 · MEMBER.USER_ID)
-- ============================================================
CREATE TABLE CONSULTATION (
    CNSL_SN        NUMBER(20)     GENERATED BY DEFAULT AS IDENTITY,
    STD_USER_ID    VARCHAR2(20)   NOT NULL,   -- 학생 (STUDENT.STD_USER_ID = MEMBER.USER_ID)
    CHRG_USER_ID   VARCHAR2(20)   NULL,       -- 담당 강사 (MEMBER.USER_ID)
    CNSL_TYPE_CD   CHAR(2)        NULL,       -- 상담 유형 (COM_CD 211)
    CNSL_STAT_CD   CHAR(2)        DEFAULT '02' NOT NULL, -- 상담 상태 (COM_CD 212)
    CNSL_DT        TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL, -- 상담 일시
    CNSL_CN        VARCHAR2(4000) NULL,       -- 상담 내용
    CNSL_SMRY      VARCHAR2(1000) NULL,       -- 상담 요약
    FLLW_UP_CN     VARCHAR2(4000) NULL,       -- 후속 조치 사항
    RGTR_ID        VARCHAR2(20)   NULL,
    REG_DT         TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL,
    LAST_MDFR_ID   VARCHAR2(20)   NULL,
    MDFCN_DT       TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NULL,
    CONSTRAINT PK_CONSULTATION PRIMARY KEY (CNSL_SN),
    CONSTRAINT FK_CNSL_STD_USER FOREIGN KEY (STD_USER_ID) REFERENCES MEMBER (USER_ID)
);

COMMENT ON COLUMN CONSULTATION.CNSL_SN      IS 'PK · IDENTITY';
COMMENT ON COLUMN CONSULTATION.STD_USER_ID  IS '학생 · MEMBER.USER_ID 참조';
COMMENT ON COLUMN CONSULTATION.CHRG_USER_ID IS '담당 강사 · MEMBER.USER_ID 참조';
COMMENT ON COLUMN CONSULTATION.CNSL_TYPE_CD IS '상담 유형 · COM_CD 211 (01 대면/02 전화/03 문자)';
COMMENT ON COLUMN CONSULTATION.CNSL_STAT_CD IS '상담 상태 · COM_CD 212 (01 예정/02 완료/03 후속조치/04 취소)';
COMMENT ON COLUMN CONSULTATION.CNSL_DT      IS '상담 일시';
COMMENT ON COLUMN CONSULTATION.CNSL_CN      IS '상담 내용';
COMMENT ON COLUMN CONSULTATION.CNSL_SMRY    IS '상담 요약';
COMMENT ON COLUMN CONSULTATION.FLLW_UP_CN   IS '후속 조치 사항';

CREATE INDEX IX_CONSULTATION_STD ON CONSULTATION (STD_USER_ID);
CREATE INDEX IX_CONSULTATION_DT  ON CONSULTATION (CNSL_DT);
