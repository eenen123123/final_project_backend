-- =====================================================================
-- 강좌 수강권한(COURSE_ENROLLMENT) 테이블
-- 결제 확정 시 (회원, 강좌) 단위로 이용 권한을 부여한다.
-- 만기일(ACCS_END_DT)은 결제 시점에 계산해서 저장 → 권한 확인은 날짜 비교 한 번
-- 재구매 시 행을 추가하지 않고 기존 행의 만기를 1년 연장(UNIQUE 제약으로 1행 보장)
-- ※ ORDERS 테이블 생성(create_orders_table.sql) 후 실행
-- =====================================================================

CREATE TABLE COURSE_ENROLLMENT (
    ENRL_SN       NUMBER(20)    GENERATED BY DEFAULT AS IDENTITY,
    USER_ID       VARCHAR2(20)  NOT NULL,
    COURSE_SN     NUMBER(20)    NOT NULL,
    ORD_SN        NUMBER(20)    NULL,
    ACCS_STRT_DT  TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ACCS_END_DT   TIMESTAMP     NOT NULL,
    ENRL_STAT_CD  VARCHAR2(10)  DEFAULT 'ACTIVE' NOT NULL,
    REG_DT        TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MDFCN_DT      TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NULL,
    CONSTRAINT PK_COURSE_ENROLLMENT PRIMARY KEY (ENRL_SN),
    CONSTRAINT UK_COURSE_ENROLLMENT UNIQUE (USER_ID, COURSE_SN),
    CONSTRAINT FK_COURSE_ENROLLMENT_MEMBER FOREIGN KEY (USER_ID) REFERENCES MEMBER (USER_ID),
    CONSTRAINT FK_COURSE_ENROLLMENT_COURSE FOREIGN KEY (COURSE_SN) REFERENCES COURSE (COURSE_SN),
    CONSTRAINT FK_COURSE_ENROLLMENT_ORDERS FOREIGN KEY (ORD_SN) REFERENCES ORDERS (ORD_SN)
);
COMMENT ON TABLE COURSE_ENROLLMENT IS '강좌 수강권한 · (회원, 강좌)당 1행';
COMMENT ON COLUMN COURSE_ENROLLMENT.ENRL_SN      IS '기본키(PK) · 시퀀스';
COMMENT ON COLUMN COURSE_ENROLLMENT.USER_ID      IS 'MEMBER.USER_ID 참조(FK)';
COMMENT ON COLUMN COURSE_ENROLLMENT.COURSE_SN    IS 'COURSE.COURSE_SN 참조(FK)';
COMMENT ON COLUMN COURSE_ENROLLMENT.ORD_SN       IS '권한을 부여/연장한 마지막 주문 ORDERS.ORD_SN 참조(FK)';
COMMENT ON COLUMN COURSE_ENROLLMENT.ACCS_STRT_DT IS '이용 시작일 (최초 결제 시점)';
COMMENT ON COLUMN COURSE_ENROLLMENT.ACCS_END_DT  IS '이용 만기일 (결제일 + 1년) · 권한 확인 기준';
COMMENT ON COLUMN COURSE_ENROLLMENT.ENRL_STAT_CD IS 'ACTIVE:이용가능 / REVOKED:회수(환불 등) (enum EnrollmentStatus)';
