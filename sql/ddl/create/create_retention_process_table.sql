-- ============================================================
-- 퇴원 방어 상담 프로세스 이력 (manager/retention.html)
--  · 근태 Excel 업로드로 위험 학생을 식별 → 상담 진행 기록
--  · 퇴원 사유 : 공통코드 203 (학생 퇴소 사유)
--  · 결과     : 공통코드 227 (01 진행중 / 02 유지 / 03 퇴원)
--  · 담당자   : CHRG_USER_ID (상담 작성 로그인 사용자)
--  · 대상     : 오프라인 학생 (MEMBER.USER_ROLE = 'ROLE_STUDENT')
-- ============================================================
CREATE TABLE RETENTION_PROCESS (
    RTNP_SN        NUMBER(20)     GENERATED BY DEFAULT AS IDENTITY,
    STD_USER_ID    VARCHAR2(20)   NOT NULL,   -- 학생 (MEMBER.USER_ID)
    WDRW_RSN_CD    CHAR(2)        NULL,       -- 퇴원 사유 (COM_CD 203)
    RTNP_CN        VARCHAR2(4000) NULL,       -- 상담 내용
    RTNP_RSLT_CD   CHAR(2)        DEFAULT '01' NOT NULL, -- 결과 (COM_CD 227)
    CHRG_USER_ID   VARCHAR2(20)   NULL,       -- 담당자 (MEMBER.USER_ID)
    RTNP_DT        TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL, -- 상담 일시
    RGTR_ID        VARCHAR2(20)   NULL,
    REG_DT         TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL,
    LAST_MDFR_ID   VARCHAR2(20)   NULL,
    MDFCN_DT       TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NULL,
    CONSTRAINT PK_RETENTION_PROCESS PRIMARY KEY (RTNP_SN),
    CONSTRAINT FK_RTNP_STD_USER FOREIGN KEY (STD_USER_ID) REFERENCES MEMBER (USER_ID)
);

COMMENT ON COLUMN RETENTION_PROCESS.RTNP_SN      IS 'PK · IDENTITY';
COMMENT ON COLUMN RETENTION_PROCESS.STD_USER_ID  IS '학생 · MEMBER.USER_ID 참조';
COMMENT ON COLUMN RETENTION_PROCESS.WDRW_RSN_CD  IS '퇴원 사유 · COM_CD 203';
COMMENT ON COLUMN RETENTION_PROCESS.RTNP_RSLT_CD IS '결과 · COM_CD 227 (01 진행중/02 유지/03 퇴원)';
COMMENT ON COLUMN RETENTION_PROCESS.CHRG_USER_ID IS '담당자 · MEMBER.USER_ID 참조';
COMMENT ON COLUMN RETENTION_PROCESS.RTNP_DT      IS '상담 일시';

CREATE INDEX IX_RETENTION_PROCESS_STD ON RETENTION_PROCESS (STD_USER_ID);
