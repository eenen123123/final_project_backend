-- =====================================================================
-- 주문(ORDERS) + 주문 상품(ORDER_ITEM) 테이블
-- 토스 결제 전 주문 선생성(PENDING) → 결제 승인 시 PAID 확정 구조
-- ※ ORDER는 Oracle 예약어라 테이블명 ORDERS 사용
-- =====================================================================

CREATE TABLE ORDERS (
    ORD_SN       NUMBER(20)     GENERATED BY DEFAULT AS IDENTITY,
    ORD_ID       VARCHAR2(100)  NOT NULL,
    USER_ID      VARCHAR2(20)   NOT NULL,
    ORD_NM       VARCHAR2(100)  NULL,
    TOT_AMT      NUMBER(15)     NOT NULL,
    ORD_STAT_CD  VARCHAR2(10)   DEFAULT 'PENDING' NOT NULL,
    REG_DT       TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MDFCN_DT     TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NULL,
    CONSTRAINT PK_ORDERS PRIMARY KEY (ORD_SN),
    CONSTRAINT UK_ORDERS_ORD_ID UNIQUE (ORD_ID),
    CONSTRAINT FK_ORDERS_MEMBER FOREIGN KEY (USER_ID) REFERENCES MEMBER (USER_ID)
);
COMMENT ON TABLE ORDERS IS '주문 헤더 · 결제 1건당 주문 1건';
COMMENT ON COLUMN ORDERS.ORD_SN      IS '기본키(PK) · 시퀀스';
COMMENT ON COLUMN ORDERS.ORD_ID      IS '토스 orderId · 서버에서 UUID로 발급 · 위변조 검증 기준';
COMMENT ON COLUMN ORDERS.USER_ID     IS 'MEMBER.USER_ID 참조(FK)';
COMMENT ON COLUMN ORDERS.ORD_NM      IS '주문명 예: 수학 정석 강의 외 2건';
COMMENT ON COLUMN ORDERS.TOT_AMT     IS '서버가 상품 가격을 재조회해 합산한 총 결제 금액';
COMMENT ON COLUMN ORDERS.ORD_STAT_CD IS 'PENDING:결제대기 / PAID:결제완료 / CANCELED:취소 (enum OrderStatus)';

CREATE TABLE ORDER_ITEM (
    ORD_ITEM_SN  NUMBER(20)     GENERATED BY DEFAULT AS IDENTITY,
    ORD_SN       NUMBER(20)     NOT NULL,
    PROD_DIV_CD  VARCHAR2(10)   NOT NULL,
    PROD_SN      NUMBER(20)     NOT NULL,
    PROD_NM      VARCHAR2(100)  NULL,
    PROD_PRICE   NUMBER(15)     NOT NULL,
    ITEM_QTY     NUMBER(5)      DEFAULT 1 NOT NULL,
    REG_DT       TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_ORDER_ITEM PRIMARY KEY (ORD_ITEM_SN),
    CONSTRAINT FK_ORDER_ITEM_ORDERS FOREIGN KEY (ORD_SN) REFERENCES ORDERS (ORD_SN)
);
COMMENT ON TABLE ORDER_ITEM IS '주문 상품 상세 · 주문 1건당 상품 N개 (강의 + 교재 등)';
COMMENT ON COLUMN ORDER_ITEM.ORD_ITEM_SN IS '기본키(PK) · 시퀀스';
COMMENT ON COLUMN ORDER_ITEM.ORD_SN      IS 'ORDERS.ORD_SN 참조(FK)';
COMMENT ON COLUMN ORDER_ITEM.PROD_DIV_CD IS 'TEXTBOOK:교재 / COURSE:강좌 (enum ProductType · CART와 동일)';
COMMENT ON COLUMN ORDER_ITEM.PROD_SN     IS '상품 SN (TEXTBOOK_SN 또는 COURSE_SN)';
COMMENT ON COLUMN ORDER_ITEM.PROD_NM     IS '주문 시점 상품명 스냅샷';
COMMENT ON COLUMN ORDER_ITEM.PROD_PRICE  IS '주문 시점 단가 스냅샷';
COMMENT ON COLUMN ORDER_ITEM.ITEM_QTY    IS '수량';
