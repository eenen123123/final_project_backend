CREATE TABLE NOTIFICATION (
    NOTI_SN       NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY,
    RCVR_USER_ID  VARCHAR2(20)    NOT NULL,
    SNDR_USER_ID  VARCHAR2(20)    NULL,
    NOTI_TYPE     VARCHAR2(10)    NOT NULL,
    NOTI_CN       VARCHAR2(500)   NOT NULL,
    LINK_URL      VARCHAR2(200)   NULL,
    RCPT_DT       TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    READ_YN       CHAR(1)         DEFAULT 'N' NOT NULL,
    READ_DT       TIMESTAMP       NULL,
    CONSTRAINT PK_NOTIFICATION PRIMARY KEY (NOTI_SN),
    CONSTRAINT FK_NOTI_RCVR FOREIGN KEY (RCVR_USER_ID) REFERENCES MEMBER (USER_ID),
    CONSTRAINT FK_NOTI_SNDR FOREIGN KEY (SNDR_USER_ID) REFERENCES MEMBER (USER_ID),
    CONSTRAINT CK_NOTI_READ_YN CHECK (READ_YN IN ('Y', 'N'))
);

COMMENT ON TABLE  NOTIFICATION              IS '알림';
COMMENT ON COLUMN NOTIFICATION.NOTI_SN      IS '알림 일련번호 (PK)';
COMMENT ON COLUMN NOTIFICATION.RCVR_USER_ID IS '수신자 아이디';
COMMENT ON COLUMN NOTIFICATION.SNDR_USER_ID IS '발신자 아이디 (시스템 알림이면 NULL)';
COMMENT ON COLUMN NOTIFICATION.NOTI_TYPE    IS '알림 유형 (예: CHAT, NOTICE, APPROVAL 등 Java Enum으로 관리)';
COMMENT ON COLUMN NOTIFICATION.NOTI_CN      IS '알림 내용';
COMMENT ON COLUMN NOTIFICATION.LINK_URL     IS '클릭 시 이동할 URL (예: /admin/chat/123)';
COMMENT ON COLUMN NOTIFICATION.RCPT_DT      IS '알림 수신 시각';
COMMENT ON COLUMN NOTIFICATION.READ_YN      IS 'Y:읽음 / N:읽지 않음';
COMMENT ON COLUMN NOTIFICATION.READ_DT      IS '읽은 시각';
